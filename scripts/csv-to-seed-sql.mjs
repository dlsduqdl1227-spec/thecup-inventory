#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const [inputPath, outputPath = './scripts/seed.sql'] = process.argv.slice(2);
if (!inputPath) {
  console.error('사용법: node scripts/csv-to-seed-sql.mjs inventory.csv scripts/seed.sql');
  process.exit(1);
}

const csv = fs.readFileSync(inputPath, 'utf8').replace(/^\uFEFF/, '');
const rows = parseCsv(csv);
if (rows.length < 2) {
  console.error('CSV 데이터가 비어 있습니다.');
  process.exit(1);
}

const header = rows[0].map((v) => v.trim());
const records = rows.slice(1).filter((row) => row.some((cell) => String(cell || '').trim() !== ''));

function value(row, names) {
  for (const name of names) {
    const idx = header.findIndex((h) => h.toLowerCase() === name.toLowerCase());
    if (idx >= 0) return String(row[idx] || '').trim();
  }
  return '';
}

const lines = [
  '-- Generated by scripts/csv-to-seed-sql.mjs',
  'BEGIN TRANSACTION;'
];

for (const row of records) {
  const item = value(row, ['item', '품목명', 'name']);
  const lot = value(row, ['lot', 'LOT']);
  const type = value(row, ['type', '구분']);
  const amount = Number.parseFloat(value(row, ['amount_kg', 'amount', '수량', 'kg'])) || 0;
  const amountMkg = Math.round(amount * 1000);
  const process = value(row, ['process', '프로세스', '가공']);
  const categoryRaw = value(row, ['category', '카테고리', '구분2']);
  const category = categoryRaw.toUpperCase() === 'ROASTED' || categoryRaw === '원두' ? 'ROASTED' : 'GREEN';
  const createdAtRaw = value(row, ['created_at', 'timestamp', 'date', '날짜']);
  const createdAt = toIsoDateTime(createdAtRaw) || new Date().toISOString();
  const expiry = value(row, ['expiry_date', '유통기한', '만료일']);
  if (!item || !lot || !type || amountMkg === 0) continue;

  lines.push(`INSERT INTO entries (id, created_at, item, lot, type, amount_mkg, expiry_date, process, category) VALUES (${[
    sql(cryptoRandomId()), sql(createdAt), sql(item), sql(lot), sql(type), String(amountMkg), expiry ? sql(expiry) : 'NULL', sql(process), sql(category)
  ].join(', ')});`);
}

lines.push('COMMIT;');
fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, lines.join('\n'));
console.log(`${records.length}개 CSV 행을 변환했습니다: ${outputPath}`);

function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = '';
  let quoted = false;
  for (let i = 0; i < text.length; i += 1) {
    const ch = text[i];
    const next = text[i + 1];
    if (quoted) {
      if (ch === '"' && next === '"') { cell += '"'; i += 1; }
      else if (ch === '"') quoted = false;
      else cell += ch;
    } else {
      if (ch === '"') quoted = true;
      else if (ch === ',') { row.push(cell); cell = ''; }
      else if (ch === '\n') { row.push(cell); rows.push(row); row = []; cell = ''; }
      else if (ch !== '\r') cell += ch;
    }
  }
  row.push(cell);
  rows.push(row);
  return rows;
}

function sql(value) {
  return `'${String(value ?? '').replace(/'/g, "''")}'`;
}

function cryptoRandomId() {
  return 'seed-' + Math.random().toString(36).slice(2) + Date.now().toString(36);
}

function toIsoDateTime(value) {
  const text = String(value || '').trim();
  if (!text) return '';
  const date = new Date(text);
  if (Number.isFinite(date.getTime())) return date.toISOString();
  const match = text.match(/^(\d{2})\.(\d{1,2})\.(\d{1,2})$/);
  if (match) {
    const date2 = new Date(Date.UTC(2000 + Number(match[1]), Number(match[2]) - 1, Number(match[3]), 0, 0, 0));
    if (Number.isFinite(date2.getTime())) return date2.toISOString();
  }
  return '';
}
